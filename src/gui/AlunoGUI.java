    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton2ActionPerformed
        AlunoDAO AlunoDAO = new AlunoDAO();
        if (jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Insira o CPF de um aluno para remover.");
            return;
        }
        String[] dados = AlunoDAO.consulta(jTextField2.getText());
        if (dados[4].equals("")) {
            JOptionPane.showMessageDialog(null, "O aluno com esse CPF não existe.");
        } else {
            AlunoDAO.remove(jTextField2.getText());
            CalculadoraIMC.removerResultado(jTextField2.getText());

            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            JOptionPane.showMessageDialog(null, "Aluno e seu historico de peso removido com sucesso");
        }
    }// GEN-LAST:event_jButton2ActionPerformed

