    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
    Aluno Aluno = new Aluno();
    AlunoDAO AlunoDAO = new AlunoDAO();
    String[] dados = AlunoDAO.consulta(jTextField2.getText());
    boolean incomplete = false;
    for (Entry<JTextField, JLabel> entrySet : map.entrySet()) {
        JTextField key = entrySet.getKey();
        JLabel value = entrySet.getValue();
        value.setText(key.getText().isEmpty() ? "*" : "");
        incomplete = incomplete ? incomplete : value.getText() == "*";
    }
    if (incomplete) {
        JOptionPane.showMessageDialog(null, "Preencha todos os campos necessários.");
    } else if (dados[4].equals("")) {
        try {
            String texto = jTextField4.getText();
            double valorDouble = Double.parseDouble(texto);
            String texto2 = jTextField5.getText();
            double valorDouble2 = Double.parseDouble(texto2);
            String dataString = jTextField3.getText();
            SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
            Date data = formato.parse(dataString);
            Aluno.setNome(jTextField1.getText());
            Aluno.setCpf(jTextField2.getText());
            Aluno.setDataNascimento(jTextField3.getText());
            Aluno.setPeso(valorDouble);
            Aluno.setAltura(valorDouble2);
            
            CalculadoraIMC.calcularIMC(Aluno);

            AlunoDAO.adiciona(Aluno);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            JOptionPane.showMessageDialog(null, Aluno.getNome() + " cadastrado com sucesso.");
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao converter data.");
        }
    } else{
        JOptionPane.showMessageDialog(null, "O aluno com esse cpf ja existe");
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
    }
}// GEN-LAST:event_jButton1ActionPerformed

